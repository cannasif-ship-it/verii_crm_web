import { type ReactElement, useState, useEffect, useMemo } from 'react';
import { useTranslation } from 'react-i18next';
import { useUIStore } from '@/stores/ui-store';
import { Plus, Search, RefreshCw, X } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { useQueryClient } from '@tanstack/react-query';
import { CustomerTypeStats } from './CustomerTypeStats';
import { CustomerTypeTable } from './CustomerTypeTable';
import { CustomerTypeForm } from './CustomerTypeForm';
import { useCreateCustomerType } from '../hooks/useCreateCustomerType';
import { useUpdateCustomerType } from '../hooks/useUpdateCustomerType';
import { useCustomerTypeList } from '../hooks/useCustomerTypeList'; // <-- Hook burada!
import type { CustomerTypeDto, CustomerTypeFormSchema } from '../types/customer-type-types';

export function CustomerTypeManagementPage(): ReactElement {
  const { t } = useTranslation();
  const { setPageTitle } = useUIStore();
  
  // State
  const [formOpen, setFormOpen] = useState(false);
  const [editingCustomerType, setEditingCustomerType] = useState<CustomerTypeDto | null>(null);
  
  const [searchTerm, setSearchTerm] = useState('');
  const [activeFilter, setActiveFilter] = useState('all'); // all, active, inactive
  const [isRefreshing, setIsRefreshing] = useState(false);

  const queryClient = useQueryClient();
  const createCustomerType = useCreateCustomerType();
  const updateCustomerType = useUpdateCustomerType();

  // --- VERİ ÇEKME (CLIENT SIDE FILTERING İÇİN TÜM DATA) ---
  const { data: apiResponse, isLoading } = useCustomerTypeList({
    pageNumber: 1,
    pageSize: 10000, // Tüm veriyi çekiyoruz
  });

  const customerTypes = apiResponse?.data || (apiResponse as any)?.items || [];

  // Sayfa Başlığı
  useEffect(() => {
    setPageTitle(t('customerTypeManagement.menu', 'Müşteri Tipi Yönetimi'));
    return () => setPageTitle(null);
  }, [t, setPageTitle]);

  // --- FİLTRELEME MANTIĞI (Memory'de) ---
  const filteredCustomerTypes = useMemo(() => {
    if (!customerTypes) return [];

    let result = [...customerTypes];

    // 1. Arama Filtresi
    if (searchTerm) {
        const lowerSearch = searchTerm.toLowerCase();
        result = result.filter(item => 
            item.name.toLowerCase().includes(lowerSearch) ||
            (item.description && item.description.toLowerCase().includes(lowerSearch))
        );
    }

    // 2. Aktif/Pasif Filtresi (IsDeleted'a göre)
    if (activeFilter === 'active') {
        result = result.filter(item => !item.isDeleted);
    } else if (activeFilter === 'inactive') {
        result = result.filter(item => item.isDeleted);
    }

    return result;
  }, [customerTypes, searchTerm, activeFilter]);

  // Handlers
  const handleAddClick = (): void => {
    setEditingCustomerType(null);
    setFormOpen(true);
  };

  const clearSearch = () => setSearchTerm('');

  const handleRefresh = async () => {
    setIsRefreshing(true);
    await queryClient.invalidateQueries({ queryKey: ['customer-types'] });
    setTimeout(() => setIsRefreshing(false), 500);
  };

  const handleEdit = (customerType: CustomerTypeDto): void => {
    setEditingCustomerType(customerType);
    setFormOpen(true);
  };

  const handleFormSubmit = async (data: CustomerTypeFormSchema): Promise<void> => {
    if (editingCustomerType) {
      await updateCustomerType.mutateAsync({
        id: editingCustomerType.id,
        data: {
          name: data.name,
          description: data.description || undefined,
        },
      });
    } else {
      await createCustomerType.mutateAsync({
        name: data.name,
        description: data.description || undefined,
      });
    }
    setFormOpen(false);
    setEditingCustomerType(null);
  };

  return (
    <div className="w-full space-y-6">
      
      {/* HEADER */}
      <div className="flex flex-col md:flex-row md:items-center justify-between gap-5 pt-2">
        <div className="flex flex-col gap-2">
          <h1 className="text-3xl font-bold tracking-tight text-slate-900 dark:text-white transition-colors">
            {t('customerTypeManagement.menu', 'Müşteri Tipi Yönetimi')}
          </h1>
          <p className="text-slate-500 dark:text-slate-400 text-sm font-medium transition-colors">
            {t('customerTypeManagement.description', 'Müşteri tiplerini yönetin ve düzenleyin')}
          </p>
        </div>
        
        <Button 
          onClick={handleAddClick}
          className="px-6 py-2 bg-gradient-to-r from-pink-600 to-orange-600 rounded-lg text-white text-sm font-bold shadow-lg shadow-pink-500/20 hover:scale-105 transition-transform border-0 hover:text-white"
        >
          <Plus size={18} className="mr-2" />
          {t('customerTypeManagement.addButton', 'Yeni Tip Ekle')}
        </Button>
      </div>

      <CustomerTypeStats />

      {/* SEARCH BAR */}
      <div className="bg-white/70 dark:bg-[#1a1025]/60 backdrop-blur-xl border border-white/60 dark:border-white/5 shadow-sm rounded-2xl p-5 flex flex-col gap-5 transition-all duration-300">
          <div className="flex flex-col lg:flex-row items-start lg:items-center justify-between gap-4">
            
            <div className="flex flex-col sm:flex-row items-stretch sm:items-center gap-3 w-full lg:w-auto">
                <div className="relative group w-full sm:w-72 lg:w-96">
                  <Search className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-slate-400 group-focus-within:text-pink-500 transition-colors" />
                  <Input
                    placeholder={t('common.search', 'Hızlı Ara...')}
                    value={searchTerm}
                    onChange={(e) => setSearchTerm(e.target.value)}
                    className="pl-10 h-10 bg-white/50 dark:bg-card/50 border-slate-200 dark:border-white/10 focus-visible:ring-0 focus-visible:ring-offset-0 focus-visible:border-pink-500 dark:focus-visible:border-pink-500 rounded-xl transition-all w-full"
                  />
                  {searchTerm && (
                    <button
                      onClick={clearSearch}
                      className="absolute right-3 top-1/2 -translate-y-1/2 p-1 hover:bg-slate-100 dark:hover:bg-white/10 rounded-full transition-colors"
                    >
                      <X size={14} className="text-slate-400" />
                    </button>
                  )}
                </div>

                <div className="flex items-center gap-3">
                  <div 
                    className="h-10 w-10 flex items-center justify-center bg-white/50 dark:bg-card/50 border border-slate-200 dark:border-white/10 rounded-xl cursor-pointer hover:border-pink-500/30 hover:bg-pink-50/50 dark:hover:bg-pink-500/10 transition-all group shrink-0"
                    onClick={handleRefresh}
                  >
                    <RefreshCw 
                      size={18} 
                      className={`text-slate-500 dark:text-slate-400 group-hover:text-pink-600 dark:group-hover:text-pink-400 transition-colors ${isRefreshing ? 'animate-spin' : ''}`} 
                    />
                  </div>
                </div>
            </div>

            <div className="flex items-center gap-1 bg-slate-100/50 dark:bg-white/5 p-1 rounded-xl w-full lg:w-auto overflow-x-auto">
               {['all', 'active', 'inactive'].map((filter) => (
                 <button
                   key={filter}
                   onClick={() => setActiveFilter(filter)}
                   className={`
                     flex-1 lg:flex-none px-4 py-1.5 rounded-lg text-xs font-bold uppercase tracking-wider transition-all whitespace-nowrap
                     ${activeFilter === filter 
                     ? 'bg-white dark:bg-[#1a1025] text-pink-600 dark:text-pink-400 shadow-sm border border-slate-200 dark:border-white/10' 
                     : 'text-slate-500 dark:text-slate-400 hover:text-slate-700 dark:hover:text-slate-200'}
                   `}
                 >
                   {filter === 'all' ? t('common.all', 'Tümü') : filter === 'active' ? t('status.active', 'Aktif') : t('status.inactive', 'Pasif')}
                 </button>
               ))}
            </div>
          </div>
      </div>

      {/* TABLO - ARTIK DUMB COMPONENT (Sadece veriyi gösterir) */}
      <div className="bg-white/70 dark:bg-[#1a1025]/60 backdrop-blur-xl border border-white/60 dark:border-white/5 shadow-sm rounded-2xl p-0 sm:p-1 transition-all duration-300 overflow-hidden">
        <CustomerTypeTable
          customerTypes={filteredCustomerTypes} // <-- FİLTRELENMİŞ VERİYİ VERİYORUZ
          isLoading={isLoading}
          onEdit={handleEdit}
        />
      </div>

      <CustomerTypeForm
        open={formOpen}
        onOpenChange={setFormOpen}
        onSubmit={handleFormSubmit}
        customerType={editingCustomerType}
        isLoading={createCustomerType.isPending || updateCustomerType.isPending}
      />
    </div>
  );
}