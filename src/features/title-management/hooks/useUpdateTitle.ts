import { useMutation, useQueryClient } from '@tanstack/react-query';
import { useTranslation } from 'react-i18next';
import { toast } from 'sonner';
import { titleApi } from '../api/title-api';
import { queryKeys, TITLE_MANAGEMENT_QUERY_KEYS } from '../utils/query-keys';
import type { UpdateTitleDto, TitleDto } from '../types/title-types';

export const useUpdateTitle = () => {
  const { t } = useTranslation();
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: ({ id, data }: { id: number; data: UpdateTitleDto }) =>
      titleApi.update(id, data),
    onSuccess: (updatedTitle: TitleDto) => {
      queryClient.invalidateQueries({ 
        queryKey: [TITLE_MANAGEMENT_QUERY_KEYS.LIST],
        exact: false,
      });
      queryClient.invalidateQueries({ queryKey: queryKeys.detail(updatedTitle.id) });
      queryClient.invalidateQueries({ queryKey: queryKeys.stats() });
      queryClient.refetchQueries({ 
        queryKey: [TITLE_MANAGEMENT_QUERY_KEYS.LIST],
        exact: false,
      });
      toast.success(t('titleManagement.messages.updateSuccess', 'Ünvan başarıyla güncellendi'));
    },
    onError: (error: Error) => {
      toast.error(error.message || t('titleManagement.messages.updateError', 'Ünvan güncellenemedi'));
    },
  });
};
